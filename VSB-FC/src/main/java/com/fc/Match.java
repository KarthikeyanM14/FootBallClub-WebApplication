package com.fc;

public class Match {
	String title,place,date;

	public Match(String title, String place, String date) {
		super();
		this.title = title;
		this.place = place;
		this.date = date;
	}

	public String getTitle() {
		return title;
	}

	public String getPlace() {
		return place;
	}

	public String getDate() {
		return date;
	}
	

}
